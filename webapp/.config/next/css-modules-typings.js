function withCSSModulesTypings(config, { dev, isServer, webpack }) {
  if (isServer || !dev) {
    return config;
  }

  const cssRule = config.module.rules.find(
    (rule) =>
      rule &&
      rule.oneOf &&
      rule.oneOf[0] &&
      rule.oneOf[0].loader === "noop-loader"
  );

  if (!cssRule) {
    return config;
  }

  const cssModule = cssRule.oneOf.find((entry) => {
    return (
      entry &&
      entry.test instanceof RegExp &&
      entry.test.test("hello.module.css") &&
      entry.use &&
      entry.use[0] &&
      entry.use[0].loader &&
      entry.use[0].loader.match(
        dev ? /next-style-loader/g : /mini-css-extract-plugin/g
      )
    );
  });
  const cssModulesTypingsLoader = {
    loader: require.resolve("@teamsupercell/typings-for-css-modules-loader"),
    options: {
      banner:
        "// Autogenerated by typings-for-css-modules-loader.\n// Please do not modify this file !\n",
      verifyOnly: !dev,
    },
  };
  if (cssModule) {
    console.debug("With CSS Module typings generator");
    cssModule.use.splice(1, 0, cssModulesTypingsLoader);

    config.plugins.push(new webpack.WatchIgnorePlugin([[/css\.d\.ts$/]]));
  }

  return config;
}

module.exports = withCSSModulesTypings;
